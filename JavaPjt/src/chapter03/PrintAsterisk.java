package chapter03;

import java.util.Scanner;

public class PrintAsterisk {
	public static void main(String[] args) {
		Scanner scan = new Scanner(System.in);
		System.out.print("정수를 입력하시오>>");
        int line = scan.nextInt();
        
        for(int i=line-1; i>=0; i--) {
            for(int j=0; j<=i; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
		scan.close();
	}
}
